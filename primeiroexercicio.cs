using System;




//somando, subtraindo, dividindo e multiplicando em c algumacoisa

int n1, n2;

Console.Write("Digite o 1º número:  ");
n1 = int.Parse(Console.ReadLine());

Console.Write("Digite o 2º número:  ");
n2 = int.Parse(Console.ReadLine());

Console.WriteLine("A soma 1º com o 2º é igual a: " + (n1+n2));
Console.WriteLine("A subtração do 1º pelo 2º é igual a: " + (n1 - n2));
